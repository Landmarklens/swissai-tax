/**
 * Get keywords for a specific page type and language
 * @param {string} pageType - Type of page (homepage, faq, features, etc.)
 * @param {string} language - Language code (de, fr, it, en)
 * @returns {string} Comma-separated keywords
 */
export const getKeywords = (pageType, language = 'de') => {
  const pageKeywords = keywords[pageType];
  if (!pageKeywords) {
    console.warn(`No keywords found for page type: ${pageType}`);
    return '';
  }

  const langKeywords = pageKeywords[language] || pageKeywords.de;
  return langKeywords.join(', ');
};

/**
 * Get multilingual keywords for a page (combines all languages)
 * @param {string} pageType - Type of page
 * @returns {string} Comma-separated keywords from all languages
 */
export const getMultilingualKeywords = (pageType) => {
  const pageKeywords = keywords[pageType];
  if (!pageKeywords) {
    console.warn(`No keywords found for page type: ${pageType}`);
    return '';
  }

  const allKeywords = [
    ...pageKeywords.de,
    ...pageKeywords.fr,
    ...pageKeywords.it,
    ...pageKeywords.en
  ];

  return allKeywords.join(', ');
};

